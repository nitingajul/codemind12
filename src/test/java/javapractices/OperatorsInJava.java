package javapractices;

public class OperatorsInJava {

	public static void main(String[] args) {

		int a = 10;

		System.out.println(a);

		System.out.println(a++);

		System.out.println(++a);

		int b = 20;
		System.out.println(b--);

		System.out.println(b);

		System.out.println(--b);

		System.out.println("###################################################");

		int num = 1;

		for (int i = num; i <= 100; i++) {
			System.out.println(i);
		}

		System.out.println("###################################################");

		int num1 = 100;

		for (int i = num1; i >= 1; i--) {
			System.out.println(i);
		}

	}

}
