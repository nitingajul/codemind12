package javapractices;

public class RelationalOperator {

	public static void main(String[] args) {

		int a = 10, b = 20;

		System.out.println(a < b);
		System.out.println(a > b);

		System.out.println(a <= b);
		System.out.println(a <= 10);

		System.out.println(a >= b);
		System.out.println(20 >= 5);

		System.out.println(a == b);
		System.out.println(10 == a);

		System.out.println(a != b);
		System.out.println(10 != a);

	}

}
